    std::vector<const COpcode*> COpcode::DEBUG_GetOpcodes()
    {
        std::vector<const COpcode *> Opcodes{};
        Opcodes.reserve(ARRAY_COUNT(GOpcodesArray));

        for (const COpcode& OpcodeRef: GOpcodesArray)
        {
            Opcodes.push_back(&OpcodeRef);
        }

        return Opcodes;
    }
